// b) Atribua valores às variáveis num1 e num2
// c) Calcule as operações de: Soma; Subtração; Multiplicação e divisão;
// e) Para cada operação mostre o valor salvo na variável resultado logo em seguida.
public class E3 {
    public static void main(String[] args) {
        double num1;
        double num2;
        double resultado;
        num1 = 3;
        num2 = 4;
        resultado = num1 + num2;
        System.out.println("Resultado: " + resultado);
        resultado = num1 - num2;
        System.out.println("Resultado: " + resultado);
        resultado = num1 * num2;
        System.out.println("Resultado: " + resultado);
        resultado = num1 / num2;
        System.out.println("Resultado: " + resultado);


    }
}
